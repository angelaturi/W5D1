class Node
  attr_reader :key
  attr_accessor :val, :next, :prev

  def initialize(key = nil, val = nil)
    @key = key
    @val = val
    @next = nil
    @prev = nil
  end

  def to_s
    "#{@key}: #{@val}"
  end

  def remove
    
    self.prev.next = (self.prev ? self.next : nil)
    self.next.prev = (self.next ? self.prev : nil)
    
  end
end

